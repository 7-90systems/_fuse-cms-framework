<?php
    /**
     *  Set up our base blocks class.
     */
    
    namespace Fuse;
    
    
    class Block {
        
        /**
         *  Object constructor.
         */
        public function __construct () {
            add_action ('init', array ($this, 'registerBlock'));
            add_action ('wp_enqueue_scripts', array ($this, 'enqueueScriptsAndStyles'));
        } // __construct ()
        
        
        
        
        /**
         *  Register our block.
         */
        public function registerBlock () {
            /**
             *  Register your block in here.
             */
        } // registerBlock ()
        
        
        
        
        /**
         *  Render the blocks contents. This function is optional and only needed if you want to change the way that the blocks content renders.
         *
         *  @param array $args The arguments for this block.
         *  @param string $content The content that was generated by the Gutenberg system.
         *
         *  return string The final HTML code for this block.
         */
        public function render ($args = array (), $content = '') {
            return $content;
        } // render ()
        
        
        
        
        /**
         *  Enqueue our front-end scripts and stylesehets.
         */
        public function enqueueScriptsAndStyles () {
            /**
             *  Add your script ands stylesheets in here.
             */
        } // enqueueScriptsAndStyles ()
        
        
        
        
        /**
         *  Get the URI of a template file.
         *
         *  Template files can be stored in the Fuse folder under /blocks/#blockname#/ or in the themes folders in the /fuseblocks/#blockname#/ folder.
         *
         *  @param string $template_file The template file name. This should be the blocks folder and the file used for rendering.
         *  @param string $fallback This is the file URI to be used if there is no other template file located. This shoudl be the full URI for the file.
         *
         *  return string The URI of the template file.
         */
        protected function _getTemplateFileUri ($template_file, $fallback = '') {
            $template = false;
            
            // Search in CHILD THEME
            if (is_child_theme ()) {
                $tmp = get_stylesheet_directory ().DIRECTORY_SEPARATOR.'fuseblocks'.DIRECTORY_SEPARATOR.$template_file;
                
                if (file_exists ($tmp)) {
                    $template = $tmp;
                } // if ()
            } // if ()
            
            // Search in MAIN THEME
            if ($template === false) {
                $tmp = get_template_directory ().DIRECTORY_SEPARATOR.'fuseblocks'.DIRECTORY_SEPARATOR.$template_file;
                
                if (file_exists ($tmp)) {
                    $template = $tmp;
                } // if ()
            } // else
            
            // If the template file is not found in the themes, search in the main plugin
            if ($template === false) {
                $tmp = FUSE_BASE_URI.DIRECTORY_SEPARATOR.'blocks'.DIRECTORY_SEPARATOR.$template_file;
                    
                if (file_exists ($tmp)) {
                    $template = $tmp;
                } // if ()
            } // if ()
            
            // If nothing else is found, use our fallback template file.
            if ($template === false) {
                $template = $fallback;
            } // if ()
            
            return $template;
        } // _getTEmplateFileUri ()
        
    } // class Block